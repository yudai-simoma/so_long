#include "libft.h"

/*
** 引数c が英数字であれば 0以外を返し、そうでなければ 0 を返す。(8進数表現)
*/
int	ft_isalnum(int c)
{
	if ((c >= 0060 && c <= 0071) || (c >= 0101 && c <= 0132)
		|| (c >= 0141 && c <= 0172))
		return (1);
	else
		return (0);
}
